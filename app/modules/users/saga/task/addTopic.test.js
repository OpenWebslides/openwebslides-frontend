// @flow

import { expectSaga } from 'redux-saga-test-plan';
import * as matchers from 'redux-saga-test-plan/matchers';
import { dynamic } from 'redux-saga-test-plan/providers';

import asyncRequests from 'modules/asyncRequests';
import topics from 'modules/topics';

import actions from '../../actions';
import * as a from '../../actionTypes';

import { sagas } from '..';

describe(`addTopic`, (): void => {

  let dummyUserId: string;
  let dummyTopicId: string;
  let dummyTitle: string;
  let dummyDescription: string;

  beforeEach((): void => {
    dummyUserId = 'dummyUserId';
    dummyTopicId = 'dummyTopicId';
    dummyTitle = 'Lorem ipsum';
    dummyDescription = 'Lorem ipsum dolor sit amet, consectetur adipiscing elit.';
  });

  it(`puts a topics CREATE action containing the passed title and description, then puts a users ADD_TOPIC_ID action containing the id generated by the api, and waits for request completion`, (): void => {
    const dummyAction = actions.addTopic(dummyUserId, dummyTitle, dummyDescription);

    return expectSaga(sagas.addTopic, dummyAction)
      .provide([
        [matchers.call.fn(asyncRequests.lib.putAndReturn), dynamic(({ args: [action] }: any, next: any): any => {
          return (action.type === topics.actionTypes.CREATE) ? { id: dummyTopicId } : next();
        })],
        [matchers.call.fn(asyncRequests.lib.putAndReturn), dynamic(({ args: [action] }: any, next: any): any => {
          return (action.type === a.ADD_TOPIC_ID) ? null : next();
        })],
      ])
      .call(asyncRequests.lib.putAndReturn, topics.actions.create(dummyTitle, dummyDescription, dummyUserId))
      .call(asyncRequests.lib.putAndReturn, actions.addTopicId(dummyUserId, dummyTopicId))
      .run();
  });

});
